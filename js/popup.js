function getRandomInt(min, max) {
  min = Math.ceil(min);
  max = Math.floor(max);
  return Math.floor(Math.random() * (max - min)) + min; //The maximum is exclusive and the minimum is inclusive
}

function setHtml(list){
	let border_color_list = ["red", "orange", "yellow", "olive", "green", "teal", "blue", "violet", "purple", "pink", "brown", "grey", "black"];
	let final_html = "";
	let name = "", fb = "", web = "";
	for (const [key, value] of Object.entries(list)) {
		name = key;
		image = (value.image === "") ? "/images/default.png" : value.image;
	  	fb = (value.fb === "") ? "#" : value.fb;
	  	instagram = (value.instagram === "") ? "#" : value.instagram;
		web = (value.web === "") ? "#" : value.web;
		border_color = border_color_list[getRandomInt(0,13)];
		default_html = '<div class="' + border_color + ' card"> <div class="ui small image"> <img src="' + image + '"/> </div> <div class="content"> <div class="center aligned header">' + name + '</div></div><div class="extra content"> <div class="center aligned"> <a href="' + fb + '" target="_blank"><i class="facebook icon"></i></a> <a href="' + instagram + '" target="_blank"><i class="instagram icon"></i> </a> <a href="' + web + '" target="_blank"><i class="linkify icon"></i></a> </div></div></div>';
		final_html += default_html;
	}
	return final_html;
}

$("#local_brand_list").html(setHtml(local_brand_list));

$("#search").change(function(){
	let new_list = {};
	for (const [key, value] of Object.entries(local_brand_list)) {
		let new_key = key.toLowerCase();
		let new_input = $(this).val().toLowerCase();
		if(new_key.indexOf(new_input) != -1){
			new_list[key] = value;
		}
	}
	$("#local_brand_list").html(setHtml(new_list));
});

